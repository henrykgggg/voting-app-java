    void insertRecord() {
    
        try {
            
            String query = "INSERT INTO voters_table (first_name, last_name, age) VALUES (?, ?, ?)";
            ps = conn.prepareStatement(query);
            ps.setString(1, first_name.getText());
            ps.setString(2, last_name.getText());
            ps.setString(3, age.getText());
            ps.execute();
            JOptionPane.showMessageDialog(null, "You are eligible to vote!");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }

